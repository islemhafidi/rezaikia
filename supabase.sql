-- ───────────────────────────────────────────────
-- 5. AUTOMATIC STORAGE BUCKET SETUP & POLICIES
-- ───────────────────────────────────────────────

-- 1. Create storage bucket 'campaign-media' if it does not exist, and ensure it is public
INSERT INTO storage.buckets (id, name, public)
VALUES ('campaign-media', 'campaign-media', true)
ON CONFLICT (id) DO UPDATE SET public = true;

-- 2. Allow public access to view/download files in 'campaign-media'
DROP POLICY IF EXISTS "Allow public select on campaign-media" ON storage.objects;
CREATE POLICY "Allow public select on campaign-media" ON storage.objects
  FOR SELECT TO public USING (bucket_id = 'campaign-media');

-- 3. Allow public uploads (insert) to 'campaign-media'
DROP POLICY IF EXISTS "Allow public insert on campaign-media" ON storage.objects;
CREATE POLICY "Allow public insert on campaign-media" ON storage.objects
  FOR INSERT TO public WITH CHECK (bucket_id = 'campaign-media');

-- 4. Allow public updates (replace) to 'campaign-media'
DROP POLICY IF EXISTS "Allow public update on campaign-media" ON storage.objects;
CREATE POLICY "Allow public update on campaign-media" ON storage.objects
  FOR UPDATE TO public USING (bucket_id = 'campaign-media') WITH CHECK (bucket_id = 'campaign-media');

-- 5. Allow public deletion from 'campaign-media'
DROP POLICY IF EXISTS "Allow public delete on campaign-media" ON storage.objects;
CREATE POLICY "Allow public delete on campaign-media" ON storage.objects
  FOR DELETE TO public USING (bucket_id = 'campaign-media');
